            KhachHang khachhang1 = new KhachHang();
            String id = txtID.getText();
            khachhang1.setId(UUID.fromString(id));
            khachhang1.setMa(txtMa.getText());
            khachhang1.setTen(txtTen.getText());

            java.util.Date ngayt = new java.util.Date();
