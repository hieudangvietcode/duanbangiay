    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if (checkDL()) {
            SimpleDateFormat df = new SimpleDateFormat("dd-MM-yyyy");
            LoaiSP loaiSP = new LoaiSP();
            loaiSP.setMa(txtMa.getText());
            loaiSP.setTen(txtTen.getText());
            int trangthai = 0;
            if (cbbTrangThai.equals("Còn hàng")) {
                trangthai = 1;
            }
            loaiSP.setTrangThai(trangthai);
            java.sql.Date ngayt = new java.sql.Date(new java.util.Date().getTime());
            loaiSP.setNgayTao(ngayt);

            String result = loaiSPService.ADD(loaiSP);
            JOptionPane.showMessageDialog(this, result);
            loadData(loaiSPService.loadData());
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        if (checkDL()) {
            int r = tblLoaiSP.getSelectedRow();
            SimpleDateFormat date = new SimpleDateFormat("dd-MM-yyyy");
            LoaiSP loaiSP1 = new LoaiSP();
            String id = txtID.getText();
            loaiSP1.setId(UUID.fromString(id));
            loaiSP1.setMa(txtMa.getText());
            loaiSP1.setTen(txtTen.getText());
            int trangthai = 0;
            if (cbbTrangThai.equals("Còn hàng")) {
                trangthai = 1;
            }
            java.util.Date ngayt = new java.util.Date();
            loaiSP1.setNgayTao(ngayt);

            java.sql.Date ngays = new java.sql.Date(new java.util.Date().getTime());
            loaiSP1.setNgaySua(ngays);

            loaiSP1.setTrangThai(trangthai);
            String result = loaiSPService.update(loaiSP1);
            JOptionPane.showMessageDialog(this, result);
            loadData(loaiSPService.loadData());
        }
    }//GEN-LAST:event_btnSuaActionPerformed

