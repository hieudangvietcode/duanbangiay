    public void loadData(List<LoaiSP> list) {
        defaultTableModel = (DefaultTableModel) tblLoaiSP.getModel();
        defaultTableModel.setRowCount(0);
        for (LoaiSP loaiSP : list) {
            defaultTableModel.addRow(new Object[]{
                loaiSP.getId(),
                loaiSP.getMa(),
                loaiSP.getTen(),
                loaiSP.getNgayTao()+"",
                loaiSP.getNgaySua()+"",
                loaiSP.hienthi()
            });
        }
    }

